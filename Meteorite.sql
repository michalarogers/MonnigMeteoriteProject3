DROP TABLE SamplesOnLoan;
DROP TABLE Loans;
DROP TABLE MeteoriteHistory;
DROP TABLE Subsamples;
DROP TABLE MeteoriteSamples;

-- Creating the MeteoriteSamples table
CREATE TABLE MeteoriteSamples (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255),
    monnigNumber VARCHAR(255) UNIQUE,
    class VARCHAR(100),
    groupType VARCHAR(100),
    clan VARCHAR(100),
    country VARCHAR(100),
    location VARCHAR(255),
    foundOrFall ENUM('Found', 'Fall'),
    sampleType VARCHAR(100),
    totalKnownWeight DECIMAL(10, 2),
    sampleWeight DECIMAL(10, 2),
    yearFound INT,
    educationalSpecimen BOOLEAN,
    repositorySpecimen BOOLEAN,
    isActive BOOLEAN DEFAULT TRUE
);

-- Creating the Subsamples table
CREATE TABLE Subsamples (
    id INT AUTO_INCREMENT PRIMARY KEY,
    meteoriteSampleId INT,
    name VARCHAR(255),
    weight DECIMAL(10, 2),
    FOREIGN KEY (meteoriteSampleId) REFERENCES MeteoriteSamples(id)
);

-- Creating the MeteoriteHistory table
CREATE TABLE MeteoriteHistory (
    id INT AUTO_INCREMENT PRIMARY KEY,
    meteoriteSampleId INT,
    date DATE,
    category ENUM('Acquired', 'Cut', 'Other'),
    details TEXT,
    FOREIGN KEY (meteoriteSampleId) REFERENCES MeteoriteSamples(id)
);

-- Creating the Loans table
CREATE TABLE Loans (
    id INT AUTO_INCREMENT PRIMARY KEY,
    loaneeName VARCHAR(255),
    institution VARCHAR(255),
    email VARCHAR(255),
    phone VARCHAR(255),
    address TEXT,
    loanStartDate DATE,
    loanDueDate DATE,
    monnigNumbersOfSamplesOnLoan TEXT,
    notes TEXT,
    archived BOOLEAN DEFAULT FALSE
);

-- Creating a table for tracking the samples on loan
CREATE TABLE SamplesOnLoan (
    id INT AUTO_INCREMENT PRIMARY KEY,
    loanId INT,
    meteoriteSampleId INT,
    FOREIGN KEY (loanId) REFERENCES Loans(id),
    FOREIGN KEY (meteoriteSampleId) REFERENCES MeteoriteSamples(id)
);

